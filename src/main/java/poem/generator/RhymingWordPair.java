package poem.generator;

public class RhymingWordPair {

    private String first;
    private String second;


    RhymingWordPair(String first, String second) {
        this.first = first;
        this.second = second;
    }


    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }
}
